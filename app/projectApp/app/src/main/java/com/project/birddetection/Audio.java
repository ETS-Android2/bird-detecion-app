package com.project.birddetection;

public class Audio {

    private String audio;
    private String format;

    public Audio() {
        this.audio = audio;
        this.format = format;
    }

    public String getAudio() {
        return audio;
    }

    public void setAudio(String audio) {
        this.audio = audio;
    }

    public String getFormat() {
        return format;
    }

    public void setFormat(String format) {
        this.format = format;
    }
}
